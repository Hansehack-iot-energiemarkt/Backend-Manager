package de.hansehack.team10.connection.api;

public class MessagePayload {
	
	private final String message;
	
	
	
	
	
	
	/**
	 * @param message
	 */
	public MessagePayload(final String message) {
		super();
		this.message = message;
	}






	public String getMessage() {
		return this.message;
	}

}
